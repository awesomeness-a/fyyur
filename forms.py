def validate_phone(self, phone):
    us_phone_num = '^([0-9]{3})[-][0-9]{3}[-][0-9]{4}$'
    match = re.search(us_phone_num, phone.data)
    if not match:
        raise ValidationError('Please, enter phone number in format xxx-xxx-xxxx')

def validate_time(form, field):
    if field.data < datetime.utcnow():
        raise ValidationError('Past events cannot be added.')
    
def validate_venue_name(form, field):
    if len(Venue.query.filter(Venue.name==form.name.data).all()) > 0:
        raise ValidationError('Venue with this name already exists.')
    
def validate_artist_name(form, field):
    if len(Artist.query.filter(Artist.name==form.name.data).all()) > 0:
        raise ValidationError('Artist with this name already exists.')


class ShowForm(FlaskForm):
    artist_id = StringField(
        'artist_id', validators=[DataRequired()]
    )
    venue_id = StringField(
        'venue_id', validators=[DataRequired()]
    )
    start_time = DateTimeField(
        'start_time',
        validators=[DataRequired(), validate_time], 
        default=datetime.today()
    )


